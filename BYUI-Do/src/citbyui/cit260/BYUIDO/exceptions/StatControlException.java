package citbyui.cit260.BYUIDO.exceptions;

/**
 *
 * @author Nick
 */
public class StatControlException extends Exception {

    public StatControlException() {
    }

    public StatControlException(String message) {
        super(message);
    }

    public StatControlException(String message, Throwable cause) {
        super(message, cause);
    }

    public StatControlException(Throwable cause) {
        super(cause);
    }

    public StatControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    
    
}
